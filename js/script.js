// -------------------- MOVING EYE --------------------

const eyes = () => {
  const esclerotica = document.querySelector(".esclerotica");
  window.addEventListener("mousemove", e => { 
    calcDegrees(e, esclerotica);
  });
};

const calcDegrees = (e, esclerotica) => {
  const getBounding = esclerotica.getBoundingClientRect();
  const distX = getBounding.width / 2 + getBounding.left;
  const distY = getBounding.height / 2 + getBounding.top;
  const rad = Math.atan2(e.y - distY, e.x - distX) 
  const degree = rad * 180 / Math.PI - 90
  
  moveEyes(degree, esclerotica);
};

const moveEyes = (degree, esclerotica) => {
  esclerotica.style.transform = `rotate(${degree}deg)`;
};

eyes();
